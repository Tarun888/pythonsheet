# arithmetic calculation using function
def add(num1,num2):
    return num1+num2
def substract(num1,num2):
    return num1-num2
def multiply(num1,num2):
    return num1*num2
def divide(num1,num2):
    return num1/num2
def modulas(num1,num2):
    return num1%num2
num1=int(input("enter the first value"))    
num2=int(input("enter the second value")) 
print("the addition is",(add(num1,num2)))
print("the substraction is",(substract(num1,num2)))
print("the multiplication is",(multiply(num1,num2)))
print("the division is",(divide(num1,num2)))
print("the modulas is",(modulas(num1,num2)))

  
